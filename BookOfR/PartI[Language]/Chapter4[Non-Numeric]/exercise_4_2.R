# a
A <- c(7, 1, 7, 10, 5, 9, 10, 3, 10, 8)
print(which((A > 5) | (A == 2)))

# b
B <- c(8, 8, 4, 4, 5, 1, 5, 6, 6, 8)
print(which((B <= 6) & (B != 4)))

# c
C_1 <- (A > 5) | (A == 2)
C_2 <- (B <= 6) & (B != 4)
print(C_1 & C_2)

# d
D <- A + B

# di
print(which((D >= 14) & (D != 15)))

# dii
print(which(((D / A) > 4) | ((D / A) <= 2)))

# e
print(((A > 5) || (A == 2)) == 
          ((A[1] > 5) || (A[1] == 2)))

print(((B <= 6) && (B != 4)) ==
          ((B[1] <= 6) && (B[1] != 4)))

print((C_1 && C_2) ==
          (C_1[1] && C_2))

